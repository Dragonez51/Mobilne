package com.example.todolist;

public class Settings {

    private int red, green, blue;

    public Settings(){
        this.red = 130;
        this.green = 255;
        this.blue = 130;
    }

    public int getRed() {
        return red;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int green) {
        this.green = green;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }
}